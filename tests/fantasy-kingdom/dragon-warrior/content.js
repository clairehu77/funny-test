// 结局判断逻辑（quiz 专属，可调用 app.js 提供的全局 has / countEarly / endings）
function chooseEnding(scores) {
  const earlyLedger   = countEarly(["ledger"]);
  const earlyExpose   = countEarly(["expose"]);
  const earlyCoop     = countEarly(["dragon", "negotiate", "practical"]);
  const earlyAutonomy = countEarly(["autonomy", "balanced"]);
  const earlySlay     = countEarly(["slay"]);

  // 隐藏结局（需要连贯主题积累 + 关键节点选项）
  if (earlyLedger >= 4 && (has(9, "B") || has(9, "C") || has(9, "F") || has(10, "E"))) return endings.tax;
  if (earlyExpose >= 4 && (has(10, "E") || has(11, "D"))) return endings.boss;
  if (earlyCoop >= 5 && has(11, "F")) return endings.shop;
  if (earlyAutonomy >= 5 && scores.slay <= 2 && (has(11, "D") || scores.autonomy >= 3)) return endings.noDragon;

  // 拔剑路线
  if (has(11, "A") && scores.slay >= 5) return endings.standard;
  if (scores.slay >= 6 && scores.investigate < 3) return endings.propaganda;
  if (scores.slay >= 5 && scores.investigate >= 2) return endings.scorched;
  if (earlySlay >= 3 && scores.practical < 2) return endings.unemployed;

  // 调查路线（先于谈判检查，避免高调查分被导向谈判结局）
  if (scores.investigate >= 5 && scores.practical < 4 && scores.dragon < 4) return endings.delay;
  if (scores.investigate <= 2 && scores.slay >= 3) return endings.pumpkin;

  // 谈判/实务路线
  if (scores.practical >= 5 && scores.negotiate >= 4) return endings.civil;
  if (scores.negotiate >= 4 && scores.balanced < 2 && scores.investigate < 3) return endings.badTalk;

  // 龙相关路线
  if (scores.dragon >= 4 && scores.negotiate < 3) return endings.roommate;
  if (scores.dragon >= 4 && scores.practical >= 3) return endings.neighbor;
  if (scores.dragon >= 3 && scores.people >= 2 && scores.expose < 2 && scores.ledger < 2) return endings.pr;
  if (scores.dragon >= 4 && scores.loyal < 2) return endings.dragonHappy;

  // 忠诚/平衡路线
  if (scores.loyal >= 3 && scores.balanced >= 2) return endings.kingHappy;
  if (scores.balanced >= 3 && scores.negotiate >= 3) return endings.bothAngry;

  // 拖延/观望路线
  if (scores.delay >= 2 && scores.negotiate < 3) return endings.gone;

  return endings.discount;
}
